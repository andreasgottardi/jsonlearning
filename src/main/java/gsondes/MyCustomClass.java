package gsondes;

public class MyCustomClass {

	String mem1;
	String mem2;
	String mem3;

	public MyCustomClass(String mem1, String mem2, String mem3) {
		this.mem1 = mem1;
		this.mem2 = mem2;
		this.mem3 = mem3;
	}

	public String getMem1() {
		return mem1;
	}

	public void setMem1(String mem1) {
		this.mem1 = mem1;
	}

	public String getMem2() {
		return mem2;
	}

	public void setMem2(String mem2) {
		this.mem2 = mem2;
	}

	public String getMem3() {
		return mem3;
	}

	public void setMem3(String mem3) {
		this.mem3 = mem3;
	}
}
